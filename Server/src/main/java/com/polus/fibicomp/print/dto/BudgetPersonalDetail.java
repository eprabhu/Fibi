package com.polus.fibicomp.print.dto;

public class BudgetPersonalDetail {

	private String personName;
	private String startDate;
	private String endDate;
	private String salary;
	private String percentageEffort;
	private String appliedSalary;
	private String costSharePercentage;
	private String costShareAmount;
	private String fundRequested;
	private String ioCode;

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getSalary() {
		return salary;
	}

	public void setSalary(String salary) {
		this.salary = salary;
	}

	public String getPercentageEffort() {
		return percentageEffort;
	}

	public void setPercentageEffort(String percentageEffort) {
		this.percentageEffort = percentageEffort;
	}

	public String getAppliedSalary() {
		return appliedSalary;
	}

	public void setAppliedSalary(String appliedSalary) {
		this.appliedSalary = appliedSalary;
	}

	public String getCostSharePercentage() {
		return costSharePercentage;
	}

	public void setCostSharePercentage(String costSharePercentage) {
		this.costSharePercentage = costSharePercentage;
	}

	public String getCostShareAmount() {
		return costShareAmount;
	}

	public void setCostShareAmount(String costShareAmount) {
		this.costShareAmount = costShareAmount;
	}

	public String getFundRequested() {
		return fundRequested;
	}

	public void setFundRequested(String fundRequested) {
		this.fundRequested = fundRequested;
	}

	public String getIoCode() {
		return ioCode;
	}

	public void setIoCode(String ioCode) {
		this.ioCode = ioCode;
	}

}
